PARTIAL = {"theclassic","hyperturbo","monsterstack","spirits"}

def struct_html(slug):
    rows=[]
    for row in STRUCTS[slug]:
        if row[0]=="##":
            rows.append(f'              <tr class="span"><td colspan="5">{esc(row[1])}</td></tr>')
        else:
            lv,ba,bl,du,tt=row
            rows.append(f'              <tr><td class="lv">{esc(lv)}</td><td>{esc(ba)}</td><td>{esc(bl)}</td><td>{esc(du)}</td><td>{esc(tt)}</td></tr>')
    srows="\n".join(rows)
    partial = slug in PARTIAL
    summary = "ストラクチャー表（Lv.1-3）" if partial else "ストラクチャー表（全レベル）"
    note = ('<p class="note" style="margin-top:12px">※ Lv.4 以降のストラクチャーは当日のストラクチャーシート／運営にてご案内します。</p>' if partial else '')
    return f"""<details class="acc" open><summary>{summary}</summary>
          <div class="acc-body" style="overflow-x:auto">
            <table class="struct">
              <thead><tr><th>Lv.</th><th>BB Ante</th><th>Blinds</th><th>Duration</th><th>Time Table</th></tr></thead>
              <tbody>
{srows}
              </tbody>
            </table>
            {note}
          </div>
        </details>"""

pat = re.compile(r'<details class="acc".*?</details>', re.S)
n=0
for slug in STRUCTS:
    f=os.path.join(ROOT,"CAJcRlRs2",slug,"index.html")
    s=open(f,encoding='utf-8').read()
    new=pat.sub(lambda m: struct_html(slug), s, count=1)
    if new!=s:
        open(f,'w',encoding='utf-8').write(new); n+=1
        lv=len([r for r in STRUCTS[slug] if r[0] not in ('##',)])
        print(f"  {slug}: {lv}Lv 反映")
    else:
        print(f"  {slug}: 置換失敗（パターン不一致）")
